package d20200701;
//2. 산술연산자 : +,-,*,/,%

public class OperEx02 {
	public static void main(String[] args) {
		int a = 100;
		int b = 200;
		
		System.out.println("a + b = " + (a+b));
		System.out.println("a - b = " + (a-b));
		System.out.println("a x b = " + (a*b));
		System.out.println("a / b = " + (a/b));
		System.out.println("a % b = " + (a%b)); 
		
	}

}
