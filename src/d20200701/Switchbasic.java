package d20200701;

public class Switchbasic {
	public static void main(String[] args) {
		
		int num = 5;
		
		switch(num) {
		case 1 :
			System.out.println("wow java");
			break;
		case 2 :
			System.out.println("Happy java");
			break;
		case 3 :
			System.out.println("Fantasy java");
			break;
		default :
			System.out.println("이건 디폴트값이야!");
		}
		
		
		
		
		
	}

}
